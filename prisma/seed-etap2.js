// --- ANA FONKSİYON ---
async function main() {
  console.log('2. Etap verisi aktarılıyor...');

  // Migration uygula
  await prisma.$executeRawUnsafe(`
    CREATE TABLE IF NOT EXISTS "new_Ogrenci_check" ("id" TEXT);
    DROP TABLE IF EXISTS "new_Ogrenci_check";
  `);

  // Telefon deduplication tracker
  const usedPhones = new Set();
  let phoneCounter = 9000;

  function uniqueTel(base) {
    let t = base.replace(/[\s\-()]/g, '').replace(/^0/, '0');
    if (!t || t === '0000000000' || t === '0000000001') {
      t = `PLACEHOLDER${phoneCounter++}`;
    }
    if (usedPhones.has(t)) {
      t = `${t}-${phoneCounter++}`;
    }
    usedPhones.add(t);
    return t;
  }

  let konutCreated = 0, ogrenciCreated = 0, sozlesmeCreated = 0, skipped = 0;

  for (const row of DATA) {
    const [blok, daire, oda, tip, ad, soyad, rawTel, bolum, kiraBedeli, tarihStr] = row;

    // Skip empty name rows (placeholders)
    if (!ad && !soyad) { skipped++; continue; }

    const daireNo = `${blok}-${daire}-${oda}`;
    const kat = katNo(daire);
    const tel = uniqueTel(rawTel);

    // 1. Konut oluştur (yoksa)
    let konut = await prisma.konut.findUnique({ where: { daireNo } });
    if (!konut) {
      konut = await prisma.konut.create({
        data: {
          blok,
          katNo: kat,
          daireNo,
          tip,
          metrekare: tip === '1+1' ? 35 : tip === '1+0' ? 25 : 20,
          kiraBedeli: kiraBedeli || 0,
          durum: kiraBedeli > 0 ? 'Dolu' : 'Bos',
          etap: 2,
          ozellikler: `ODA ${oda}`,
        }
      });
      konutCreated++;
    }

    // 2. Ogrenci oluştur
    let ogrenci;
    try {
      // Check if tenant already exists by phone (approximate dedup)
      ogrenci = await prisma.ogrenci.create({
        data: {
          ad: ad.trim(),
          soyad: soyad.trim(),
          telefon: tel,
          bolum: bolum || null,
          cinsiyet: null,
          universite: 'Sakarya Üniversitesi',
        }
      });
      ogrenciCreated++;
    } catch (e) {
      // Probably duplicate phone; try with suffix
      try {
        ogrenci = await prisma.ogrenci.create({
          data: {
            ad: ad.trim(),
            soyad: soyad.trim(),
            telefon: `${tel}-${phoneCounter++}`,
            bolum: bolum || null,
            cinsiyet: null,
            universite: 'Sakarya Üniversitesi',
          }
        });
        ogrenciCreated++;
      } catch (e2) {
        console.warn(`  ⚠ Ogrenci atlandı: ${ad} ${soyad} (${tel}): ${e2.message}`);
        skipped++;
        continue;
      }
    }

    // 3. Sozlesme oluştur (kira bedeli varsa)
    if (kiraBedeli > 0) {
      const baslangic = parseTarih(tarihStr);
      const bitis = new Date(baslangic);
      bitis.setFullYear(bitis.getFullYear() + 1);
      const sozNo = `E2-${blok}${daire}-${oda}-${Date.now()}-${Math.random().toString(36).substr(2,4)}`;

      try {
        await prisma.sozlesme.create({
          data: {
            sozlesmeNo: sozNo,
            konutId: konut.id,
            ogrenciId: ogrenci.id,
            baslangicTarihi: baslangic,
            bitisTarihi: bitis,
            aylikKira: kiraBedeli,
            depozito: kiraBedeli,
            durum: 'Aktif',
          }
        });
        sozlesmeCreated++;
        // Konut durumunu güncelle
        await prisma.konut.update({ where: { id: konut.id }, data: { durum: 'Dolu' } });
      } catch (e) {
        console.warn(`  ⚠ Sozlesme oluşturulamadı: ${sozNo}: ${e.message}`);
      }
    }
  }

  console.log(`✓ Konut: ${konutCreated}, Öğrenci: ${ogrenciCreated}, Sözleşme: ${sozlesmeCreated}, Atlanan: ${skipped}`);
  await prisma.$disconnect();
}

main().catch(e => { console.error(e); prisma.$disconnect(); process.exit(1); });
