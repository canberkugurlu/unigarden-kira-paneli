// --- YARDIMCI FONKSİYONLAR ---
function parseKira(str) {
  if (!str) return 0;
  return parseFloat(str.replace(/\./g, '').replace(',', '.').replace(/[^0-9.]/g, '')) || 0;
}

function parseTarih(str) {
  if (!str || str.trim() === '') return new Date('2024-09-15');
  const parts = str.trim().split('.');
  if (parts.length !== 3) return new Date('2024-09-15');
  const [d, m, y] = parts;
  const year = parseInt(y);
  if (year > 2030 || year < 2020) return new Date('2024-09-15');
  return new Date(`${y}-${m.padStart(2,'0')}-${d.padStart(2,'0')}`);
}

function katNo(daire) {
  const m = daire.match(/^(\d+)/);
  return m ? parseInt(m[1]) : 1;
}

function normTel(t) {
  if (!t) return '0000000000';
  return t.replace(/[\s\-()]/g, '');
}

